package day04_VariableCont;

public class StudentInfo {

    public static void main(String[] args) {

        String studentName = "Ali";
        int age = 22;
        char gender = 'm';
        String schoolName = "uni";
        String phoneNumber = "(555)-888-000-9999";
        boolean isFullTime = true;

        System.out.println("1.studentName = " + studentName);
        System.out.println("2.age = " + age);
        System.out.println("3.gender = " + gender);
        System.out.println("4.schoolName = " + schoolName);
        System.out.println("5.phoneNumber = " + phoneNumber);
        System.out.println("6.isFullTime = " + isFullTime);
    }

}
