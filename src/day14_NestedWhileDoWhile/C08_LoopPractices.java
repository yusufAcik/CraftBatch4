package day14_NestedWhileDoWhile;

public class C08_LoopPractices {

    public static void main(String[] args) {

        // 1-10 yazdıralım for, while ve do while ile

        for (int i=1; i<=10; i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("-----------------");

        int j=1;
        while (j<=10){
            System.out.print(j+" ");
            j++;
        }


        System.out.println();
        System.out.println("--------------------");



        int k =1;
        do{
            System.out.print(k+" ");
            k++;
        }while(k<=10);

        // do while loopu conditionu sağlamazsa bile bir kez çalışır






    }
}
