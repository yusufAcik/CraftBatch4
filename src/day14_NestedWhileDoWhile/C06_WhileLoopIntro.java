package day14_NestedWhileDoWhile;

public class C06_WhileLoopIntro {

    public static void main(String[] args) {

        if(true){
            System.out.println("hello world");
        }

        // if döngüsü 1 kere yazar

        System.out.println("----------------");

        while(true){
            System.out.println("hello world");

        }

        // while döngüsü sonsuza kadar yazar


    }

    }



