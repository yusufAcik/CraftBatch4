package day08_ScannerAndOperators;

public class C02_BirthDay {

    public static void main(String[] args){

        String name = "John";
        int birthDay = 23;
        String monthOfBirth = "April";
        int birthYear = 1990;

        System.out.println(name+" was born on "+monthOfBirth+"/"+birthDay+"/"+birthYear);
        System.out.println(name+" was born on "+"\""+monthOfBirth+"\\"+birthDay+"\\"+birthYear+"\"");






    }
}
